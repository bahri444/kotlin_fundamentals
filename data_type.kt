fun main(){
    // var nama_variabel: Type_data = value => struktur penulisan variabel

    // inisialisasi variabel dengan keyword var
    var myAdress: String ="melar, desa selebung rembiga kecamatan janapria"
    
    println(myAdress)

    // inisialisaisi variabel dengan keyword val
    val myAdres: String ="Pujut, Dusun Melar"
    println(myAdres)

    // menggabungkan 2 variabel dengan type data string
    val firstName: String = "Saepul "
    val lastName: String ="Bahri"
    println("Nama saya : " + firstName + lastName)

    // jika menggabungkan 2 variabel dengan tipe data INT maka akan melakukan operasi sesuai dengan operator yang di gunakan
    val bilangan_1=4
    val bilangan_2=4
    println(bilangan_1 + bilangan_2)
}